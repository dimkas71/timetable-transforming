package ua.compservice.model;

public class Employee {

	private String name;
	private String personnelNumber;
	private String position;
	
	public Employee() {
		
	}
	
	public Employee(String name, String personnelNumber, String position) {
		super();
		this.name = name;
		this.personnelNumber = personnelNumber;
		this.position = position;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPersonnelNumber() {
		return personnelNumber;
	}

	public void setPersonnelNumber(String personnelNumber) {
		this.personnelNumber = personnelNumber;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((personnelNumber == null) ? 0 : personnelNumber.hashCode());
		result = prime * result + ((position == null) ? 0 : position.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Employee other = (Employee) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (personnelNumber == null) {
			if (other.personnelNumber != null)
				return false;
		} else if (!personnelNumber.equals(other.personnelNumber))
			return false;
		if (position == null) {
			if (other.position != null)
				return false;
		} else if (!position.equals(other.position))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Employee [name=" + name + ", personnelNumber=" + personnelNumber + ", position=" + position + "]";
	}
		
	
}
